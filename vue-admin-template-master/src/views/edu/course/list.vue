<template>
  <div class="app-container">
    课程列表

    <!-- 查询表单 -->
    <el-form :inline="true" class="demo-form-inline">
      <el-form-item>
        <el-input v-model="courseQuery.title" placeholder="课程名称"/>
      </el-form-item>

      <el-form-item>
        <el-select v-model="courseQuery.status" placeholder="课程状态">
          <el-option :value="1" label="已发布"></el-option>
          <el-option :value="2" label="未发布"></el-option>
        </el-select>
      </el-form-item>

      <el-form-item label="添加时间">
        <el-date-picker
          v-model="courseQuery.begin"
          type="datetime"
          placeholder="选择开始时间"
          value-format="yyyy-MM dd-HH:mm:ss"
          default-time="00:00:00">
        </el-date-picker>
      </el-form-item>

      <el-form-item>
        <el-date-picker
          v-model="courseQuery.end"
          type="datetime"
          placeholder="选择截止时间"
          value-format="yyyy-MM dd-HH:mm:ss"
          default-time="00:00:00">
        </el-date-picker>
      </el-form-item>

      <el-button type="primary" icon="el-icon-search" @click="getListByCondition">查询</el-button>
      <el-button type="default" @click="resetData()">清空</el-button>
    </el-form>

    <!-- 表格 -->
    <el-table
      v-loading="listLoading"
      :data="list"
      element-loading-text="数据加载中"
      border
      fit
      highlight-current-row>

      <el-table-column
        label="序号"
        width="70"
        align="center">
        <template slot-scope="scope">
          {{ (page - 1) * limit + scope.$index + 1}}
        </template>
      </el-table-column>

      <el-table-column prop="title" label="课程名称" width="180"/>

      <el-table-column label="课程状态" width="80">
        <template slot-scope="scope">
          {{ scope.row.status === 'Normal'? '已发布':'未发布' }}
        </template>
      </el-table-column>

      <el-table-column prop="lessonNum" label="课时数" width="120"/>
      <el-table-column prop="gmtCreate" label="添加时间" width="180"/>
      <el-table-column prop="viewCount" label="浏览数量" width="120"/>

      <el-table-column label="操作" align="center">
        <template slot-scope="scope">
          <router-link :to="'/teacher/edit/' + scope.row.id">
            <el-button type="primary" size="mini" icon="el-icon-edit">编辑课程基本信息</el-button>
          </router-link>
          <router-link :to="'/teacher/edit/' + scope.row.id">
            <el-button type="primary" size="mini" icon="el-icon-edit">编辑课程大纲</el-button>
          </router-link>
          <el-button type="danger" size="mini" icon="el-icon-delete"
                     @click="removeCourse(scope.row.id)">删除课程信息</el-button>
        </template>
      </el-table-column>
    </el-table>

<!--     分页条 -->
    <el-pagination background
                   :current-page="page"
                   :page-size="limit"
                   :total="total"
                   style="padding: 30px 0; text-align: center;"
                   layout="total, prev, pager, next, jumper"
                   @current-change="getList"
    />

  </div>
</template>

<script>
// 引入course.js文件
import courseApi from '../../../api/edu/course'

export default {
  // 写代码核心位置
  data() { //定义变量和初始值
    return {
      list: null, //查询之后接口返回集合
      page: 1, //当前页
      limit: 10, //每页记录数
      total: 0, //总记录数
      courseQuery: {}, //条件封装的对象
    }
  },
  created() { //页面渲染之前，一般调用method定义的方法
    //调用
    this.getList()
  },
  methods: {
    getList(page = 1) {
      this.page = page
      console.log("change.................." + this.page)

      courseApi.getListCourseByCondition(this.page, this.limit, this.courseQuery)
        .then(response => { //请求成功
          this.total = response.data.total
          this.list = response.data.rows
        })
    },
    getListByCondition() {
      console.log(this.courseQuery)
      courseApi.getListCourseByCondition(this.page, this.limit, this.courseQuery)
        .then(response => {
          this.total = response.data.total
          this.list = response.data.rows
        })
    },
    //删除课程方法
    removeCourse(id) {
      this.$confirm('此操作将永久删除讲师记录, 是否继续?', '提示', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
      }).then(() => { //点击确定执行then
          // 调用删除方法
        courseApi.removeCourseById(id)
          .then(response => {
            // 提示信息
            this.$message({
              type: 'success',
              message: '删除成功!'
            });
            // 重新刷新列表页面
            this.getList()
          })
      })
    },

    resetData() { //清空方法
      // 1.把表单输入项清空
      this.courseQuery = {}
      // 2.查询所有的数据
      this.getList()
    }
  }
}
</script>
